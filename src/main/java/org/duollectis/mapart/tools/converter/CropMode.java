package org.duollectis.mapart.tools.converter;

/**
 * Режим подгонки исходного изображения под целевой размер карт.
 * <p>
 * STRETCH  — простое растяжение без сохранения пропорций (поведение по умолчанию).
 * CENTER   — масштабирование с сохранением пропорций и обрезкой по центру.
 * MANUAL   — пользователь вручную задаёт смещение кропа перетаскиванием на превью.
 * FIT      — картинка вписывается целиком, размер карт подбирается автоматически,
 *            пустые области заполняются прозрачным/чёрным фоном.
 */
public enum CropMode {
	STRETCH,
	CENTER,
	MANUAL,
	FIT
}
